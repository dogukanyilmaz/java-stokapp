    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        mesaj_label.setText("");
        
        // ürün ekleme butonuna basıldığında
        
        String urun_adi = urunname_input.getText().toString();
        String urun_barkod = urunbarkod_input.getText().toString();
        String urun_fiyat = urunfiyat_input.getText().toString();
        String urun_adet = urunadet_input.getText().toString();
        
        //Hata arama 
        if (urun_adi.trim().equals("")) {
            mesaj_label.setText("Ürün adı boş girilemez!");
        } else if (urun_barkod.trim().equals("")) {
            mesaj_label.setText("Ürün barkodu boş girilemez!");
        } else if (urun_fiyat.trim().equals("")) {
            mesaj_label.setText("Ürün fiyatı boş girilemez!");
        } else if (urun_adet.trim().equals("")) {
            mesaj_label.setText("Ürün adedi boş girilemez!");
        } else if (int_sorgula(urun_adet)) {            
            mesaj_label.setText("Ürün adedi yazımı hatalı!");      
        } else if (double_sorgula(urun_fiyat)) {
            mesaj_label.setText("Ürün fiyatı yazımı hatalı!");
        } else {            
            Object[] eklenecek = {urun_adi,urun_barkod,urun_fiyat,urun_adet};            
            urun_tablosu_model.addRow(eklenecek);
            mesaj_label.setText("Ürün başarıyla eklendi!");
            urunname_input.setText("");
            urunbarkod_input.setText("");
            urunfiyat_input.setText("");
            urunadet_input.setText("");
        }     
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // ürün güncelleme işlemleri
        int row = urun_tablosu.getSelectedRow();
        if (row == -1) {
            if (urun_tablosu_model.getRowCount() == 0) {
                mesaj_label.setText("Tabloda güncellenecek veri yok!");
            } else {
               mesaj_label.setText("Lütfen güncellenecek ürünü seçiniz!");
            }
        } else {
            
              
        String urun_adi = urunname_input.getText().toString();
        String urun_barkod = urunbarkod_input.getText().toString();
        String urun_fiyat = urunfiyat_input.getText().toString();
        String urun_adet = urunadet_input.getText().toString();
        
        //Hata arama 
        if (urun_adi.trim().equals("")) {
            mesaj_label.setText("Ürün adı boş girilemez!");
        } else if (urun_barkod.trim().equals("")) {
            mesaj_label.setText("Ürün barkodu boş girilemez!");
        } else if (urun_fiyat.trim().equals("")) {
            mesaj_label.setText("Ürün fiyatı boş girilemez!");
        } else if (urun_adet.trim().equals("")) {
            mesaj_label.setText("Ürün adedi boş girilemez!");
        } else if (int_sorgula(urun_adet)) {            
            mesaj_label.setText("Ürün adedi yazımı hatalı!");      
        } else if (double_sorgula(urun_fiyat)) {
            mesaj_label.setText("Ürün fiyatı yazımı hatalı!");
        } else {           

            urun_tablosu_model.setValueAt(urun_adi,row, 0);
            urun_tablosu_model.setValueAt(urun_barkod,row, 1);
            urun_tablosu_model.setValueAt(urun_fiyat,row, 2);
            urun_tablosu_model.setValueAt(urun_adet,row, 3);            
            mesaj_label.setText("Ürün başarıyla güncellendi!");
            urunname_input.setText("");
            urunbarkod_input.setText("");
            urunfiyat_input.setText("");
            urunadet_input.setText("");
        } 
        
            
            
        }
        
        
      
        
    }//GEN-LAST:event_jButton2ActionPerformed

