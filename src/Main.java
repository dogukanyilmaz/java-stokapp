    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // ürün satılmaya tıklandığında
         int row = urun_tablosu.getSelectedRow();
        if (row == -1) {
            if (urun_tablosu_model.getRowCount() == 0) {
                mesaj_label.setText("Tabloda satılacak veri yok!");
            } else {
               mesaj_label.setText("Lütfen satılacak ürünü seçiniz!");
            }
        } else {
            String urun_adedi = urun_tablosu_model.getValueAt(row, 3).toString();
            String urun_adi = urun_tablosu_model.getValueAt(row, 0).toString();
            int urun_adedi_int = Integer.parseInt(urun_adedi);
            if (urun_adedi_int > 0) {           
            urun_adedi_int--;
            if (urun_adedi_int == 0) {
                JOptionPane.showMessageDialog(this, urun_adi + " elimizde kalmadı!");
            }
            urun_tablosu_model.setValueAt(urun_adedi_int, row, 3);            
            mesaj_label.setText(urun_adi + " başarıyla satıldı");
            } else {
                mesaj_label.setText("Bu ürün stokta yok!");
            }
        }
        
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

